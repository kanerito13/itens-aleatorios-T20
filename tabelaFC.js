
function ARM() {
      const tabela = document.getElementById("tb-arma");
      const linhas = tabela.getElementsByTagName("tr");

      const indiceAleatorio = Math.floor(Math.random() * linhas.length);
      const celula = linhas[indiceAleatorio].getElementsByTagName("td")[0];

      function mostrarPopup(mensagem) {
      document.getElementById("mensagemPopup").textContent = mensagem;
      document.getElementById("popup").style.display = "block";
       }

      if (celula) {
        mostrarPopup( celula.textContent);}

      function fecharPopup() {
      document.getElementById("popup").style.display = "none";
       }

      window.onclick = function(event) {
      const modal = document.getElementById("popup");

      if (event.target == modal) {
        modal.style.display = "none";
        }
       }
      }

